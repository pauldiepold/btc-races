// Walking, Inline-Skating, Rollstuhl und "Sonstiges" sind keine Lauf-Disziplinen
const NON_RUNNING_S_CODES = new Set([
  'SWALKING', 'SNWALKING', 'SWANDERN', 'SINLINE', 'SROLLSTUHL', 'SWANDERNWALKING', 'SSONSTIGES',
])

/**
 * Gibt true zurück wenn der LADV-Disziplin-Code eine Lauf-Disziplin ist.
 * Relevant für BTC als Laufverein: Bahn, Straße, Staffeln, Zeitläufe.
 * Ausgeschlossen: Hürden, Gehen, Sprünge, Würfe, Mehrkämpfe, Rollstuhl, KiLa.
 */
export function isRunningDiscipline(code: string): boolean {
  if (NON_RUNNING_S_CODES.has(code)) return false
  return (
    code.startsWith('L') // Bahn-Lauf
    || code.startsWith('S') // Straßenlauf (Cross, Trail, Marathon …)
    || code.startsWith('X') // Staffeln
    || (code.startsWith('A') && !code.startsWith('AD')) // Zeitläufe (ASTD, AHSTD …), nicht ADM-Mannschaft
  )
}
